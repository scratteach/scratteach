// ゴールデンサンプルをプロンプト注入用のMarkdownに整形する。
export function buildGoldenSampleSection(genreId) {
  const gs = GOLDEN_SAMPLES[genreId];
  if (!gs) return '';
  const spritesText = gs.sprites
    .map(s => `▶ スプライト「${s.name}」\n\`\`\`\n${s.blocks}\n\`\`\``)
    .join('\n\n');
  const varsText = gs.variables
    .map(v => `・${v.name}：${v.purpose}　→ 表示：${v.display ? '☑' : '□（非表示）'}`)
    .join('\n');
  return [
    `## 【完全見本（実機検証済みゴールデンサンプル）】${gs.label}`,
    `※ ${gs.verifiedOn}。この見本はそのまま組めば確実に遊べる。`,
    '',
    '### 見本の使い方（絶対ルール）',
    '1. ブロックはゼロから合成せず、必ずこの見本を土台にする。ユーザー仕様と食い違う部分（列数・速度・点数・アイテムの有無など）だけを差し替え、それ以外は見本のまま写す。',
    '2. 修正・作り直しのときも、前回の出力に継ぎ足さず「この見本＋これまでの要望の合計」から組み直す。壊れた出力を土台にしない。',
    '3. 見本の反射・当たり判定・起動順序・リセットの構造（メッセージの流れ）は完成済みの正解。ユーザーの要望がない限り変更しない。',
    '4. 見本には星（パワーアップアイテム）が入っている。ユーザーがアイテムを求めていない場合は、星スプライトと関連要素（星出現・パドル拡大・星x・星y・ブロック側の1/20判定）を丸ごと外す。',
    '5. スプライト名（ボール・パドル・ブロック・星）はユーザー指定があれば置換してよいが、ブロック内の参照名も必ず一緒に置換する。',
    '',
    '### 見本の仕様要点',
    gs.spec.map(s => `- ${s}`).join('\n'),
    '',
    '### 事前準備（この見本で必要な変数・メッセージ）',
    '■ 変数（すべて全体用）',
    varsText,
    `■ メッセージ：${gs.messages.join(' / ')}`,
    '■ 音：ボール=Coin / ブロック=Tada（なければ好きな音でよい）',
    '',
    '### 見本ブロック（スプライト別・このまま土台にする）',
    spritesText,
  ].join('\n');
}
