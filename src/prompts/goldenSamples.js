// ゴールデンサンプルをプロンプト注入用のMarkdownに整形する。
export function buildGoldenSampleSection(genreId) {
  const gs = GOLDEN_SAMPLES[genreId];
  if (!gs) return '';
  const spritesText = gs.sprites
    .map(s => `▶ スプライト「${s.name}」\n\`\`\`\n${s.blocks}\n\`\`\``)
    .join('\n\n');
  const varsText = gs.variables
    .map(v => `・${v.name}：${v.purpose}　→ 表示：${v.display ? '☑' : '□（非表示）'}`)
    .join('\n');
  return [
    `## 【完全見本（実機検証済みゴールデンサンプル）】${gs.label}`,
    `※ ${gs.verifiedOn}。この見本はそのまま組めば確実に遊べる。`,
    '',
    '### 見本の使い方（絶対ルール）',
    '1. ブロックはゼロから合成せず、必ずこの見本を土台にする。ユーザー仕様と食い違う部分（数・速度・点数・アイテムの有無など）だけを差し替え、それ以外は見本のまま写す。',
    '2. 修正・作り直しのときも、前回の出力に継ぎ足さず「この見本＋これまでの要望の合計」から組み直す。壊れた出力を土台にしない。',
    '3. 見本の当たり判定・反射・起動順序・リセットの構造（メッセージの流れ）は完成済みの正解。特に「当たって消える＋得点／ダメージ」を片側のスプライトに一元化している設計（触れた判定を両側に置かない）は崩さない。ユーザーの要望がない限り変更しない。',
    `4. このジャンルの見本に含まれるスプライトは【${gs.sprites.map(s => s.name).join('・')}】。ユーザーが求めていない要素（例：パワーアップ等のおまけスプライト）は、そのスプライトと関連する変数・メッセージ・判定を丸ごと外してよい。`,
    '5. スプライト名はユーザー指定があれば置換してよいが、ブロック内の参照名（「○○に触れた」「○○へ行く」等）も必ず一緒に置換する。',
    '',
    '### 見本の仕様要点',
    gs.spec.map(s => `- ${s}`).join('\n'),
    '',
    '### 事前準備（この見本で必要な変数・メッセージ）',
    '■ 変数（表示するもの）',
    varsText,
    ...(gs.variablesNote ? [`■ そのほかの変数：${gs.variablesNote}`] : []),
    `■ メッセージ：${gs.messages.join(' / ')}`,
    `■ 音：${gs.sounds || '（なければ好きな音でよい）'}（なければ好きな音でよい）`,
    '',
    '### 見本ブロック（スプライト別・このまま土台にする）',
    spritesText,
    ...(Array.isArray(gs.orientationNote) && gs.orientationNote.length
      ? ['', '### バリエーション（向き・数などの変え方）', ...gs.orientationNote]
      : []),
  ].join('\n');
}
