// ゲームの種類テキストから該当ジャンルを推定する（キーワード一致）。
// 複数該当時は最初の一致を返す。該当なしは null。
export function detectGenre(gameTypeText = '') {
  const text = String(gameTypeText).toLowerCase();
  for (const tpl of GENRE_TEMPLATES) {
    if (tpl.keywords.some(kw => text.includes(kw.toLowerCase()))) {
      return tpl;
    }
  }
  return null;
}

// 1ジャンルのテンプレをMarkdownに整形する（プロンプト／QA注入用）。
export function renderGenreTemplate(tpl) {
  if (!tpl) return '';
  const mech = tpl.mechanics.map(m => `- [ ] ${m}`).join('\n');
  const pit = tpl.pitfalls.map(p => `- ${p}`).join('\n');
  return [
    `## ジャンルテンプレ：${tpl.label}`,
    '',
    '### 必須メカニクス（質問せず必ず実装する成立条件）',
    mech,
    '',
    '### 定番Scratchイディオム（正解形の手本）',
    '```',
    tpl.idioms,
    '```',
    '',
    '### よくある失敗（このどれかに当てはまったら作り直す）',
    pit,
  ].join('\n');
}

// 全ジャンルのテンプレをMarkdownに整形する。
export function renderAllGenreTemplates() {
  return GENRE_TEMPLATES.map(renderGenreTemplate).join('\n\n---\n\n');
}

